BEGIN
		SELECT 
		    pr.id_producto,
		    pr.clave AS clave_producto,
		    pr.nombre AS nombre_producto,
		    pr.precio,
		    pr.es_activo,
		    pp.id_producto_proveedor,
		    pp.clave_proveedor,
		    pp.costo,
		    pv.id_proveedor,
			pv.nombre AS nombre_proveedor
		FROM producto pr
		JOIN producto_proveedor pp ON pr.id_producto = pp.id_producto
		JOIN proveedor pv ON pp.id_proveedor = pv.id_proveedor;
	END ;;
