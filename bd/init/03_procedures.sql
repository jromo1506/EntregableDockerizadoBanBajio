CREATE  PROCEDURE `sp_filtrar_catalogo`(
    IN p_id_tipo_producto BIGINT,
    IN p_es_activo TINYINT,
    IN p_busqueda VARCHAR(100)
)
BEGIN
    SELECT
        pr.id_producto              AS idProducto,
        pr.clave                    AS claveProducto,
        pr.nombre                   AS nombreProducto,
        pr.precio,
        pr.es_activo                AS esActivo,

        pp.id_producto_proveedor    AS idProductoProveedor,
        pp.clave_proveedor          AS claveProveedor,
        pp.costo,

        pv.id_proveedor             AS idProveedor,
        pv.nombre                   AS nombreProveedor
    FROM producto pr
    JOIN producto_proveedor pp ON pp.id_producto = pr.id_producto
    JOIN proveedor pv ON pv.id_proveedor = pp.id_proveedor
    WHERE
        (p_id_tipo_producto IS NULL OR pr.id_tipo_producto = p_id_tipo_producto)
    AND (
        p_es_activo IS NULL
        OR pr.es_activo = p_es_activo
    )
    AND (
        p_busqueda IS NULL OR p_busqueda = ''
        OR pr.nombre LIKE CONCAT('%', p_busqueda, '%')
        OR pr.clave LIKE CONCAT('%', p_busqueda, '%')
        OR pp.clave_proveedor LIKE CONCAT('%', p_busqueda, '%')
        OR pv.nombre LIKE CONCAT('%', p_busqueda, '%')
    );
END ;;
